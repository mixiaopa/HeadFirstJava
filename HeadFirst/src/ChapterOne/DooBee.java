package ChapterOne;

public class DooBee {
    public static void main (String[] args) {
        int x = 1;
        while (x < 5) {
            System.out.println("Doo");
            System.out.println("Bee");
            x = x+1;
        }
        if (x == 5) {
            System.out.println("Do");
        }
    }
}
